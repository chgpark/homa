char	*ft_strncat(char *dest, char *src, int nb)
{
	char	*tmp;

	tmp = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0' && nb > 0)
	{
		*dest++ = *src++;
		nb--;
	}
	*dest = '\0';
	return (tmp);
}
