char		*ft_strrev(char *str)
{
	int		i;
	char	tmp;
	char	*end;

	i = 0;
	tmp = *str;
	while (tmp > 0)
	{
		i++;
		tmp = *(str + i);
	}
	end = str + i - 1;
	i = i / 2;
	while (i > 0)
	{
		tmp = *(str + i - 1);
		*(str + i - 1) = *(end - i + 1);
		*(end - i + 1) = tmp;
		i--;
	}
	return (str);
}
