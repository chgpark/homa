void	ft_sort_integer_table(int *tab, int size)
{
	int i_1;
	int i_2;
	int s;

	i_1 = 0;
	while (i_1 < (size - 1))
	{
		i_2 = 0;
		while (i_2 < (size - i_1 - 1))
		{
			if (tab[i_2] > tab[i_2 + 1])
			{
				s = tab[i_2];
				tab[i_2] = tab[i_2 + 1];
				tab[i_2 + 1] = s;
			}
			i_2++;
		}
		i_1++;
	}
}
