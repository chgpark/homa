#include "ft_lib.h"

int		ft_div(int a, int b)
{
	return (a / b);
}

int		ft_mod(int a, int b)
{
	return (a % b);
}

int		ft_usage(int a, int b)
{
	a = 0;
	b = 0;
	return (a + b);
}
