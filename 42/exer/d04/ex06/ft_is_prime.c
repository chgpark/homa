int		ft_is_prime(int nb)
{
	long n;

	n = 5;
	if (nb <= 1)
		return (0);
	if (nb <= 3)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while (n * n <= nb)
	{
		if (nb % n == 0 || nb % (n + 2) == 0)
			return (0);
		n += 6;
	}
	return (1);
}
