int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int i_1;
	int i_2;

	i_1 = 0;
	while (i_1 <= 98)
	{
		i_2 = i_1 + 1;
		while (i_2 <= 99)
		{
			ft_putchar(i_1 / 10 + '0');
			ft_putchar(i_1 % 10 + '0');
			ft_putchar(' ');
			ft_putchar(i_2 / 10 + '0');
			ft_putchar(i_2 % 10 + '0');
			if (i_1 != 98)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			i_2++;
		}
		i_1++;
	}
}
