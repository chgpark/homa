#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;
	if (n < 0)
	{
		c = '-';
		write(1, &c, 1);
		if (n < -9)
			ft_putnbr(n / -10);
		c =  -(n % 10) + '0';
		write(1, &c, 1);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		c = n % 10 + '0';
		write(1, &c, 1);
	}
	else
	{
		c = n + '0';
		write(1, &c, 1);
	}
}
